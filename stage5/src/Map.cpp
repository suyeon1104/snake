//bgMap 설정
void Map::setbgMap(const vector<vector<int>> &inputMapData)
{
    bgMap.mapDatum = inputMapData;
    int nrow = bgMap.mapDatum.size();
    int ncol = bgMap.mapDatum[0].size();
    for (int i = 0; i < nrow; i++)
    {
        for (int j = 0; j < ncol; j++)
        {
            if (bgMap.mapDatum[i][j] != 0 && bgMap.mapDatum[i][j] != 1 && bgMap.mapDatum[i][j] != 2)
            {
                bgMap.mapDatum[i][j] = 0;
            }
        }
    }
}

// mapDatum의 행 수 반환
int Map::getRowSize()
{
    return mapDatum.size();
}

// mapDatum의 열 수 반환
int Map::getColSize()
{
    return mapDatum[0].size();
}

// map 객체에서 인덱스 연산자를 이용해 직접 mapDatum에 접근하도록 만듦
vector<int> &Map::operator[](int idx)
{
    return mapDatum[idx];
}

// Map 객체의 mapDatum를 이용해 맵 보여주기
void Map::display(WINDOW *win)
{
    int nrow = mapDatum.size();
    int ncol = mapDatum[0].size();
    for (int i = 0; i < nrow; i++)
    {
        for (int j = 0; j < ncol; j++)
        {
            if (mapDatum[i][j] == 0)
            {
                // 공백이 아닌 다른 문자로 print 해줘야 snake 흔적을 지워줌.
                wattron(win, COLOR_PAIR(CP_BKGR));
                mvwprintw(win, i, j * 2, "M");
                mvwprintw(win, i, j * 2 + 1, "M");
                wattroff(win, COLOR_PAIR(CP_BKGR));
            }
            else if (mapDatum[i][j] == 1)
            {
                wattron(win, COLOR_PAIR(CP_WALL));
                mvwprintw(win, i, j * 2, "\u25A0");
                wattroff(win, COLOR_PAIR(CP_WALL));
            }
            else if (mapDatum[i][j] == 2)
            {
                wattron(win, COLOR_PAIR(CP_IMMUNE_WALL));
                mvwprintw(win, i, j * 2, "\u25A0");
                wattroff(win, COLOR_PAIR(CP_IMMUNE_WALL));
            }
        }
    }
    wrefresh(win);
}
